    <h2 style="margin-left: 5px; margin-right: 5px;">&bull; Building Tools for Sovereignty&bull;</h2>

<p style="text-align: justify; margin-left: 5px; margin-right: 5px; margin-bottom: 5px;">Over the coming decades, blockchain technology is poised to enable an entirely new sort of freedom for people globally.</p>

      <p style="text-align: justify; margin-left: 5px; margin-right: 5px; margin-bottom: 5px;">Freedom /ˈfriːdəm/</p>

<p style="text-align: justify; margin-left: 30px; margin-right: 5px; margin-bottom: 5px;">a. The quality or state of being free: such as the absence of necessity, coercion, or constraint in choice or action</p>

<p style="text-align: justify; margin-left: 30px; margin-right: 5px; margin-bottom: 5px;">b. Sovereignty; Work Sovereignty, Financial Sovereignty, Geographic Sovereignty</p>

<p style="text-align: justify; margin-left: 5px; margin-right: 5px; margin-bottom: 5px;">To obtain freedom, we need to have sovereignty; ultimate and un-confiscatable authority over ourselves. The ability to make decisions for ourselves, free of coercion.</p>

<p style="text-align: justify; margin-left: 5px; margin-right: 5px; margin-bottom: 5px;">Sovereignty can be broken down into three pillars: financial sovereignty, work sovereignty, and geographic sovereignty.</p> 

<p style="text-align: justify; margin-left: 5px; margin-right: 5px; margin-bottom: 5px;"><strong>Financial sovereignty</strong> is having control over your own savings and investments, free of centralized intermediaries. Cryptocurrency and decentralized finance  are the tools that have allowed for the realization of financial sovereignty.</p> 

<p style="text-align: justify; margin-left: 5px; margin-right: 5px; margin-bottom: 5px;">Without sovereignty of finances, centralized intermediaries such as banks, nation-states, and corporations can confiscate wealth for any reason.</p> 

<p style="text-align: justify; margin-left: 5px; margin-right: 5px; margin-bottom: 5px;"><strong>Work sovereignty</strong> is having control over how you make income to the extent that your earning ability is resilient amidst outside forces. This is made possible when one’s reputation, built over time, is trusted by enough people to make finding new work easy. Unfortunately, reputation is hard to scale; it's only been possible to scale reputation beyond <a target=_blank href="https://en.wikipedia.org/wiki/Dunbar%27s_number" class="text-info">Dunbar’s Number</a> through "trusted" (centralized) intermediaries.</p>

<p style="text-align: justify; margin-left: 5px; margin-right: 5px; margin-bottom: 5px;">Without sovereignty of work, the ability to acquire new work whenever you please, nation-states, employers, and job platforms can coerce you by rescinding work permits, firing or threatening to fire you, or banning you from job platforms. I am currently working on this problem area, applying blockchain technology to create less siloed and more accessible labor markets: <a href="https://www.talentlayer.org/"><strong>Talent</strong>Layer</a>.</p> 

<p style="text-align: justify; margin-left: 5px; margin-right: 5px; margin-bottom: 5px;"><strong>Geographic sovereignty</strong> is having the ability to easily change your physical location. Moving jurisdictions allows you to evade war, censorship, and coercion and go where you are treated best.</p>

<p style="text-align: justify; margin-left: 5px; margin-right: 5px; margin-bottom: 5px;">Geographic sovereignty is enabled by work sovereignty and financial sovereignty. Having sufficient savings and ease of making more income lowers the switching costs associated with moving jurisdictions. In the direst situations, people will free their geographic area with nothing in order to stay alive - but the lower the switching cost between jurisdictions the less coercion and negative situations people will put up with.</p>

<p style="text-align: justify; margin-left: 5px; margin-right: 5px; margin-bottom: 5px;">With the ability to assuredly generate income (work sovereignty), store wealth (financial sovereignty), and physically move to optimal places (geographic sovereignty), true freedom can be recognized. Coercion and manipulation of people with all three becomes extremely difficult.</p>

<p style="text-align: justify; margin-left: 5px; margin-right: 5px; margin-bottom: 40px;">This is what we're shooting for. This is the future we are building.</p>



