          Do you know one of the biggest barriers to the adoption of emerging tech right now?
          <br><br>
          Notice it once, and you start to notice it everywhere.<br>
          <br>
          It’s this: Talking too much about the technology in buyer communications, and not enough about the problems the technology is solving for the consumer.<br>
          <br>
          When I hear people pitching blockchain and crypto tech proofs of concepts, this pattern is all too common.<br>
          <br>
          For example; A company that is trying to sell an IOT and blockchain integrated solution for supply chain will have a salesperson who is super passionate about how you can use blockchain technology to capture data from IoT devices to make processes more efficient. When they walk in and try to pitch manufacturers for the solution, they talk all about how blockchain is the newest and best technology, that it’s the top technology skill on LinkedIn going into 2021, that their node hosting infrastructure uses the most cutting edge innovations, that distributed ledgers are going to revolutionize every industry on the earth, etc. What they don’t talk about enough is how it actually helps the client.<br>
          <br>
          Instead, frame blockchain within the context of concepts that businesses intimately relate to:<br>
          <br>
          <ul class="text-left">
            <li>Automation (Blockchain can play a key role in your automation strategy — cutting-costs.)</li>
            <li>Competitiveness (Your competitors are doing blockchain. If you aren’t, you lose.)</li>
            <li>Accuracy and Verifiability (Blockchain increases the accuracy of data stored and allows for seamless data transfer.)</li>
          </ul>
          <p class="text-left"><br>Frame absolutely everything within the context of what the end benefit is for the client. Blockchain technology allows them to expediently communicate with all of their vendors and perform transactions that took twice as long prior. In this way, they become more competitive through having better processes internally.<br>
          <br>
          For example; If you’re offering a digital credentialing project targeting healthcare companies with digital IDs of the future, you want to start by talking about reduced cost and increased efficiency. Stay out of the trap ten thousands of other tech solutions have been unable to avoid — focusing too much on the technology and not enough on the value.<br><br></p>
        <br>
        <h3>Using the Three Levels of Sales to Inspire Value Discussions</h3>
        <br>
        <p class="text-left"><span style = "font-style: italic;">
          It can be useful to frame your conversations using The Three Levels of Sales, as described in The Challenger Sale. Each level offers different information to the buyer. The three levels are: teach for differentiation, tailor the message, and take control of the sale. The third one, taking control of the sale, is the most compelling specifically to the decision-maker who doesn’t have the time or attention to figure out how things work and why they need it.
        </span><br>
          <br>
          <strong>Level 1: Teach for Differentiation</strong><br>At this level, you’re simply trying to persuade a potential buyer to get excited over your product, rather than what the competition offers. This often includes negotiation and discourse about the different attributes of the product. Getting stuck on level one means you remain focused on the technology and not the problem you are solving.<br>
          <br>
          <strong>Level 2: Tailor the Message</strong>
          Start addressing needs specific to the buyer. Instead of trying to sell a product, open up and ask questions. Find out what they need and who the decision-makers are. Do your research to see how your products will provide solutions that improve efficiency or automation. Shift focus into a problem-solver mentality rather than trying to make a sale as in level one.<br>
          <br>
          <strong>Level 3: Take Control of the Sale</strong> 
          When you take control of the sale, you drive momentum and handle objections with ease. If you have one big reason they’re going to buy from you, that usually speaks to the decision-maker. The people in power will be compelled because you’ve moved past persuasion to actively engage the buyer in a discourse that shows how well the product fits their needs and why it goes beyond the technology. Taking control of the sale means you know why your solution will work for the buyer and you convey that with confidence.<br>
          <br></p>
          <h3>Adding Value Above All</h3>
          <p class="text-left">
          Walking through these three sales levels will push you to pitch blockchain in a way that is compelling to your clients. When you get down to it, people don’t care about blockchain. What they do care about is what blockchain will do for them. Show how you can help paper-based clients leapfrog and automate, or how you help clients with legacy tech streamlining and you will have much greater reach.<br>
