        <h3 class="text-left" style="margin-top: 50px;">Who Needs a Seat at Your Enterprise DLT’s Table?</h3>
        <h6 class="text-left">Posted on Dec 3 2020</h6>
        <br><br>
        <p class="text-left">
          Here’s the situation:
          <br><br>
          You’re building a blockchain-based solution that will revolutionize the way your industry does business.<br>
          <br>
          A few early partners have committed to help launch your multi-stakeholder solution.<br>
          <br>
          Together, you’ve begun to recruit other industry players interested in storing data on the blockchain and maintaining your #singlesourceoftruth.<br>
          <br>
          Things are pretty early-stage, but it finally looks like a full-fledged proof of concept is in sight.<br>
          <br>
          Now that you’ve got a couple of committed partners on board, you’re eager to get started <strong>designing your platform, developing your tech,</strong> and  <strong>determining your governance.</strong>
          While it feels natural to rush from planning into doing once you’ve captured the attention of a few key partners, successful blockchain projects require early and active input from a <span class = "bold-italic">representative</span> group of stakeholders. Your solution should be designed with your future stakeholders in mind, not just your current early partners.<br>
          <br>
          We’ve all heard horror stories of DLT solutions that showed promise right up until the moment the platform tried to move past proof of concept. Founding stakeholders were happy with the platform, but they found it extremely difficult to recruit additional members (who were skeptical of the platform, and its ability to uphold their interests).<br>
          <br>
          <strong>Don’t let this happen to your DLT solution.</strong><br><br></p>
        <br>
        <h3>Stakeholders Make or Break Your Blockchain Solution.</h3>
        <br>
        <p class="text-left"><span style = "font-style: italic;">
          Before we cover how to prevent stakeholder apathy in your project by bringing the right groups to the table, 
          let’s briefly cover how they should be involved once they get there.
        </span><br>
          <br>
          Blockchain projects that work have a representative set of stakeholders advising on design and governance. 
          You’re engaging them, asking questions, feeling out what they really think, and determining what risks they worry about 
          so you can mitigate them. This ensures you’re putting together a workable solution all stakeholders feel comfortable using.<br>
          <br>
          Of equal importance: bringing stakeholders into the conversation early virtually guarantees buy-in later. 
          Through the act of participating in your design and governance conversations, stakeholders perceive you to be interested 
          in their needs and preferences. This instills the confidence in the system necessary for success.<br>
          <br>
          <strong>Bringing them to the table like this can be done informally or formally.</strong> 
          Informal stakeholder communication involves getting to know them and understanding their needs. 
          For formal discussion, start an advisory group that meets on a regular basis. Usually, a combination of formal 
          and informal is ideal. The point is, you have stakeholders, and you’re treating them like it.<br>
          <br></p>
          <figure>
          <img src = "img/table_stock.jpeg" style="width: 100%; height: auto; padding-bottom: 40px; padding-top: 60px;" alt="Table" class = "img-center">
          <figcaption>Your table can be big or small, formal or informal. 
            What matters is you are bringing together a representative group to inform your design, 
            development, and governance decisions.</figcaption>
          </figure>
          <br>
          <p class="text-left">
          We know what to do once we’ve got the right parties at the table — but how do we know we’ve recruited who we need?<br>
          <br>
          <strong>Do you have the right stakeholders at the table to begin having important conversations and contributing to key decisions?</strong><br>
          <br>
          Let’s find out.<br>
          </p>
        <br>
        <h3>Are the Right People at The Table? Ask These 3 Questions to Find Out.</h3>
        <br>
        <p class = "text-left">
          These three questions will help you determine if you’ve started conversations with the right stakeholders 
          for your enterprise DLT project. If you don’t, we’ll get clear on how you can find them.</p>
          <br><br>
          <h4 class="text-left"><span class = bold>1. Who Do You Eventually Want at the Table?</span></h4>
          <p class="text-left">
          What companies, entities, or people do you want involved in your ecosystem? Decide this even before embarking on design and development. 
          It’s starting with the end in mind — the tried-and-true approach to managing any project well.
          <br><br>
          Consider the alternative. If you’re unclear who you eventually want speaking into or guiding your project, 
          then in all likelihood, your governance and system design will not be tailored to the needs of those future stakeholders. 
          You’ll either have to alter the system or alter the governance. <span class="bold-italic">This is an expensive mistake to make.</span>
          <br><br>
          Let’s examine a scenario; an agriculture supply chain startup in Guatemala is designing a multi-stakeholder consortium of 
          agricultural companies to build and maintain a shared source of truth to preserve and share information. From the get-go, 
          the startup needs to determine who they want on the platform in its early stage, late stage, and mature stage.
          <br><br>
          <span class = "bold-italic amplify">How Do You Determine Who You Want at the Table?</span>
          <br>
          I tell clients to create a <strong>DLT Actor Map</strong> to ensure they have a holistic 
          understanding of their actors, and how they intend to have them engage with their system.</p>
          <br>
          <figure>
          <img src="img/dltActorMap.png" alt = "DLT Actor Map" style="width: 100%; height: auto; padding-bottom: 40px; padding-top: 60px;" class="img-center">
          </figure>
          <br>
          <p class = "text-left">
          <strong>A DLT Actor Map is a Venn Diagram that maps out ecosystem parties who affect governance decisions and ecosystem 
          parties who are affected by those decisions.</strong> The “Actor” circle includes those who affect governance decisions 
          (including “operators”, who affect but are not affected by, and “stakeholders”, who affect and are affected by). 
          The “Member” circle includes those who are affected by governance decisions (including “stakeholders”, who affect 
          and are affected by, and “betroffenen”, who do not affect but are affected by).
          <br><br>
          An example of an <strong>operator</strong> is a state regulator — any government that regulates produce would impact the startup.
          <br><br>
          An example of a <strong>stakeholder</strong> is a farmer who uses the system — they engage directly with the platform, 
          participate in decision making (governance) and are impacted by the outcomes of the platform.
          <br><br>
          <span class="bold-italic">Betroffenen</span> is a German term that loosely means 
          “anyone in a situation without a say about the situation.” 
          In this ecosystem, that’s third-party vendors and arguably the most important individuals of all — people who buy produce.
          <br><br>
          The parties who are housed within a completed <strong>DLT Actor Map</strong> are the parties necessary for your project’s success.
          <br><br>
          They are who you need to be engaging as early as possible.
          </p>
          <br>
          <br>
        <h4 class="text-left"><span class = "bold">2. When Will They Arrive At The Table, and How Does That Impact Their Feelings?</span></h4>
        <p class="text-left">
          <a href = "https://en.wikipedia.org/wiki/Technology_adoption_life_cycle">Diffusion of innovation theory</a> states that
          every market has different types of adopters; 
          innovators, early adopters, early majority, late majority, and laggards. 
          They all want different things from products, and in this case, your blockchain platform.</p>
          <figure>
          <img src="img/techAdoptionCycle.png" alt="Technology Adoption Cycle" style="width: 100%; height: auto; padding-bottom: 40px; padding-top: 60px;" class = "img-center">
          <figcaption>Thank you Wikimedia Commons for this diagram!</figcaption>
          </figure>
          <br>
          <p class="text-left">
          Often, platform earlier adopters have very different desires and fears than late adopters. 
          If you only design, build, and make decisions with part of the desired collective at the table early on, 
          you risk later adopters at best feeling stigmatized and wary that their interests won’t be considered or at worst 
          not gaining value from the platform due to design flaws.
          <br><br>
          Diversity among early decision-makers and informers is essential, particularly when building for mature industries 
          like pharmaceuticals. Consider another scenario: an EU-based, industry-wide pharma startup developing an ingredient 
          traceability network. They absolutely need representation from both the “innovator” type pharma companies and more 
          conservative “late adopter” type companies in their advisory group so they’re empowered to build a product and governance 
          system suitable for all eventual stakeholders. If the startup only considered people currently engaging with their platform 
          as partners or advisory council members, they’d skew towards innovators, who have very different needs and risk tolerances 
          than conservative companies.
          
          <br><br>
          <span class = "bold-italic amplify">How Do You Recruit Representative Engagers?</span>
          <br>
          Your governance and solution should be designed with input (or at the very least sentiment analysis) from both the 
          earlier adopters and the later adopters in this process.
          <br><br>
          This is easier said than done. Laggards and Late Majority adopters, deserving of their titles, prefer to wait until 
          it is inevitable to adopt a novel solution before considering it.
          <br><br>
          Try to recruit representatives that reflect all types of adopters who will engage with your platform to participate 
          in an advisory group. It is difficult to get traditionally late adopting parties involved early — seeking industry 
          advisors with the ability to articulate the interests of these late stakeholders can be necessary to substitute for 
          direct input. If you cannot recruit these representatives, delegate “roleplaying” to someone on your team; this 
          individual’s job will be to research and “act” as if they were that adopter type, understanding and vouching for both 
          their needs and concerns.
          <br><br>
          In the end, it’s worth it to bring in all the right people from the very beginning. Are they at your table yet?
          <br><br>
        </p>
      <h4 class="text-left"><span class = bold>3. Are you Leaving Anyone Important Out?</span></h4>
      <p class="text-left">
        It’s always surprising when I find an advisory group or consortium in a highly regulated industry that 
        excludes regulators and existing industry associations. These parties are often left out of the actor analyses 
        and early conversations by DLT consortia and projects.
        <br><br>
        These existing groups do matter, even if they are not the target members of your consortium or are desired clients to 
        use your product. They at the least have a second-hand relationship with your project and may even have a direct impact on 
        its operations.
        <br><br>
        In Singapore, a country with some of the most stringent financial technology regulations, let’s look at a notable 
        DLT FinTech platform: our next scenario. It was founded by five banks to co-create a mutually maintained security 
        tokenization platform. At the beginning, the banks wisely decided to invite regulatory body representatives into their
        monthly advisory meetings. Transparency and a willingness to help regulators to do their job encourages all network 
        participants to ensure that everything they build maintains complete compliance.
       <br><br>
       <span class = "bold-italic amplify">How Do You Get Regulatory and Industry Group Actors Involved?</span></p>
       <br>
      <figure>
       <img src="img/regulatory.jpeg" style="width: 100%; height: auto; padding-bottom: 40px; padding-top: 60px;" alt ="regulatory" class="img-center">
       <figcaption>Starting conversations with existing industry groups and prominent regulatory bodies can seem daunting, 
         but a little engagement can go a long way when it comes to forging partnerships on behalf of your DLT solution.</figcaption>
      </figure>
      <br>
      <p class="text-left">
      If you’re operating in a mature industry with prominent existing players and associations, be inclusive. 
      Show them you have no intention of stepping on their feet but rather you desire to act in a complementary way that upholds 
      their interests (when possible). Getting along with industry groups can make your job easier — they have oftentimes concentrated
      groups of your target stakeholders, and partnerships with these groups can make recruiting engaged adopters a breeze.
      <br><br>
      Set the stage for encouraging regulatory compliance by involving regulators in your activities. Earn regulators’ favor by 
      showing how you are making their job easier. Simply invite regulators to send representatives to your advisory meetings or 
      pass along updates as your project progresses. A little goes a long way.
      <br><br>
      </p>

