<h1 class="dfs mb-5" style="padding-bottom: 5px">The TalentLayer Manifesto: Enabling Freedom Through Work Reputation</h1>


<p class="mb-5" style="padding-bottom: 5px">Reputation and the ability to find work are deeply intertwined.</p>

<p class="mb-5" style="padding-bottom: 5px">What controls your ability to find new work? Your reputation and the systems it relies on.</p>

<h1 class="dfs mb-5" style="padding-bottom: 5px">Scaling reputation</h1>
                    
<p class="mb-5" style="padding-bottom: 5px">With a good reputation in your city, country, industry, or online ecosystem, finding new work is easy.</p>

<p class="mb-5" style="padding-bottom: 5px">Verifiable work is the bedrock of reputation. This is why you have the strongest reputations with people you have worked with directly or with a few degrees of separation; it is easy to find people to attest that you have the ability to deliver - they’ve seen you do it or know the person who’s seen you do it. Unfortunately, these sorts of reputations don’t scale beyond <a target=_blank href="https://en.wikipedia.org/wiki/Dunbar%27s_number" class="text-info">Dunbar’s Number</a>.</p>

<p class="mb-5" style="padding-bottom: 5px">This is also why freelancing and online work platforms have been able to create such powerful reputation networks; reputations are based on almost exclusively verifiable work that has been done within the platform ecosystem. The platform itself becomes the attester that you have delivered work - because people trust the platform, these reputations are more scaleable, but also more vulnerable. At the will of a company’s internal policy change, a <a target=_blank href="lost.php" class="text-info">geography’s new sanctions law</a>, or a platform’s bankruptcy, your work reputation can be obliterated from existence. </p>

<p class="mb-5" style="padding-bottom: 5px">Is it possible to scale reputation past Dunbar’s number without a middleman? </p>

<p class="mb-5" style="padding-bottom: 5px">How can we prove without a doubt that work has been done, in a way that is universally recognized?</p>

<h1 class="dfs mb-5" style="padding-bottom: 5px">Freedom through work sovereignty</h1>
<p class="mb-5" style="padding-bottom: 5px">Freedom /ˈfriːdəm/  </p>
<p class="mb-5" style="padding-bottom: 5px">a. The quality or state of being free: such as the absence of necessity, coercion, or constraint in choice or action</p>
<p class="mb-5" style="padding-bottom: 5px">b. Sovereignty; Work Sovereignty, Financial Sovereignty, Geographic Sovereignty</p>

<p class="mb-5" style="padding-bottom: 5px">Work Sovereignty is having complete ownership and control of your ability to find new work through a self-custodied and interoperable work reputation. It enables Financial Sovereignty (ability to store and grow wealth without intermediaries) and Geographic Sovereignty (ability to change your location when needed) through allowing one to acquire wealth, regardless of one's life situation.</p>

<p class="mb-5" style="padding-bottom: 5px">Work Sovereignty will unleash a whole new era of freedom for humanity. </p>

<h1 class="dfs mb-5" style="padding-bottom: 5px">Tools for work sovereignty</h1>
<p class="mb-5" style="padding-bottom: 5px">The reputation system that enables Work Sovereignty must be… </p>

<p class="mb-5" style="padding-bottom: 5px"><strong>Verifiable:</strong> Based on verifiable work and reviews</p>

<p class="mb-5" style="padding-bottom: 5px"><strong>Unconfiscateable:</strong> Self-owned and self-custodied, unable to be taken away from you </p>

<p class="mb-5" style="padding-bottom: 5px"><strong>Incorruptible:</strong> Unable to be edited, immutable</p>

<p class="mb-5" style="padding-bottom: 5px"><strong>Accessible:</strong> Available to anyone</p>

<p class="mb-5" style="padding-bottom: 5px"><strong>Interoperable:</strong> Easily integrated and widely recognized</p>

<p class="mb-5" style="padding-bottom: 5px"><strong>Community-maintained:</strong> Open-source and decentralized </p>


<h1 class="dfs mb-5" style="padding-bottom: 5px">Building the future of work</h1>
<p class="mb-5" style="padding-bottom: 5px"><strong>Talent</strong>Layer is an interoperable, decentralized, infrastructure layer for building talent marketplaces, reputation systems, and the future of work. </p>

<p class="mb-5" style="padding-bottom: 5px">At its core, <strong>Talent</strong>Layer leverages blockchain technology to provide a verifiable, unconfiscateable, incorruptible, accessible, interoperable, and community-owned work reputation system. </p>

<p class="mb-5" style="padding-bottom: 5px">A user’s <strong>Talent</strong>Layer ID reputation grows through the user’s interactions on any <strong>Talent</strong>Layer integrated marketplaces and work platforms. A <strong>Talent</strong>Layer identity can hire or be hired. Multiple <strong>Talent</strong>Layer identities create associations representing DAOs, companies, dev shops, and other forms of collectives. Associations can hire or be hired.</p> 

<p class="mb-5" style="padding-bottom: 5px"><strong>Talent</strong>Layer will transform the talent marketplace ecosystem into a sustainable, interoperable, integrated economy of scale.</p>

