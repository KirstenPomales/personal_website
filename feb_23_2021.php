        <h3 class="text-left" style="margin-top: 50px;">The Flip Side of Funding: Grants for Blockchain Builders</h3>
        <h6 class="text-left">Posted on Feb 23 2021</h6>
        <br>
        <p class="text-left">
		In the early stages of your startup’s journey, it's only natural to worry about funding; whether you are bootstrapping or trying to scale a venture-backed unicorn. 
		In the blockchain space, the word “funding” often evokes images of VC firms, ICOs, and hackathon prize winnings. <b>There’s one funding strategy for builders in the blockchain 
		space that I think could use some more love: grants.</b></br></br>
	</p>
	<p class="text-left">
		<h4 class="text-left">Grants Galore</b></b></h4>
	</p>
	<p class="text-left">
		In 2021 there are seemingly endless grant programs seeking hungry projects to fund.</br></br>
		Most of these grant programs are affiliated with specific chain ecosystems. These types of programs want you to build on their chain, use their integration, or somehow collaborate with their system — they offer grants to encourage this engagement. 
		In return for building something that meets the criteria for the grant, they’ll give you capital. The amount given as grants widely varies depending on the program; I’ve seen some programs offering $1,000 and other programs offering $20,000 or more.</br></br>
		These grants can be a really great opportunity to get your project off the ground with an initial team and initial proof of concept, without digging too deep into your own pockets. <b>Even if you don’t want to build your project on a specific chain offering a grant, some of these grants are flexible enough that you can make them work for you.</b>
		<i>For example, you have started building on Ethereum, are committed to continuing to build on Ethereum, but want to take advantage of a grant that Tezos is offering. If you are able to meet the criteria of the Tezos grant while still building most of your project on Ethereum, you could still apply for the grant.</i></br></br>
		In contrast to seeking VC, another benefit is that most of these grant programs don’t take any equity. If you’re in the very early stage and you don’t want to be handing out large chunks of equity to a VC firm, then going for these grants can be a really great option.</br></br>
		Grants are actually pretty easy to find if you know what you’re looking for.</br></br>
	</p>
	<p class="text-left">
		<b>Know Your Project</b>
		</br>Start with an awareness of what your project is and what direction you can take it in to find appropriate grants (your whole teem needs to be on the same page). 
		What are you doing and how will funding help? It’s easy to search grants, if you can pinpoint attributes of those that will work for you.
	</p>
	<p class="text-left">
		<b></br>Know Your Flexibility</b>
		</br>You want to determine what your <b>window of possibility</b> is, as far as pivoting in a direction that a specific fund wants you to go. 
		Discover, wherever possible, if you can pivot to access funding and hit the ground running, even if it requires a few changes. 
		While you may need to tweak certain aspects of your project to meet funding requirements, it might be worth it for the capital investment. 
		Figure out your willingness to move in different directions as a team, then determine which funds you think will be a good fit.</br></br>
	</p>	
		<img style="width: 100%; height: auto" src="img/flipimg1.jpeg" alt="kirstenpomales">
		</br><p>Finding grants that fit may take some time, or you might find a match right away (depending on how picky you are)!</p></br></br>
	<p class="text-left">
		<h4 class="text-left">How to Find Grants for Blockchain Tech</b></b></h4>
	</p>
	<p class="text-left">
		There is a lot of availability of funding for blockchain builders so grants are easy to find and accessible for a wide range of projects. 
		Search online for foundations and grants that fit the projects you are working on. Finding grants that fit may take some time, or you might find a match right away. Everyone is different and grant options are continuing to expand and change.</br></br>
		Here are three examples of resources and grant programs offering funding to blockchain tech projects:</br></br>
	</p>
	<p class="text-left">
		&nbsp;&nbsp;<b>1. <u><a href = "https://gitcoin.co/grants/">Gitcoin</a></u></b></br></br>
		Gitcoin allows funders to post their grant requirements and grant seekers can search for opportunities that best fit their project. 
		Gitcoin is a platform for grant seekers and funders to find each other, with a 0.00001% fee that covers costs. Since it is community based, it is a more efficient method of funding than typical in-house or foundation grants. 
		Funding amounts are variable. Gitcoin is beneficial for those looking to decentralize and move to a more independent model.</br></br>
	</p>
	<p class="text-left">
		&nbsp;&nbsp;<b>2. <u><a href = "https://esp.ethereum.foundation/en/">Ethereum Foundation</a></u></b></br></br>
		Projects supporting Ethereum and related technologies can receive funding and non-financial support. EF strives to accelerate the growth of the Ethereum ecosystem. 
		The grant program has expanded to include ongoing guidance, especially open source projects focusing on universal tools, infrastructure, research and public goods. 
		Funding ranges from $10,000 to $1,500,000.</br></br>
	</p>
	<p class="text-left">
		&nbsp;&nbsp;<b>3. <u><a href = "https://research.protocol.ai/categories/grants/">Protocol Labs</a></u></b></br></br>
		Protocol Labs Research grant program supports projects focusing on computation, communication, and knowledge. 
		They are particularly interested in approving grants for expanding user-inspired research, and the future direction of blockchain. 
		Funding is between $200,000 and $5,000,000 depending on the extent of the project. Ongoing RFPs detail the specifics for each grant cycle.
	</p>
	<p class="text-left">
		<b></br>Get That Grant!</b>
		</br>Once you have a grant in mind, find out as much as you can about it. Research past funded projects to determine an appropriate approach. 
		Keep up to date on the grant deadlines and requirements. Find out who the key decision makers are for the foundation or grant, as well as contact information. 
		Before you apply, try to reach out to managers and other people that work there via LinkedIn or email. Try to see if there is anyone in your network who can give you an introduction. 
		Otherwise, try some cold outreach. If you don’t hear a reply, just apply.</br></br>
		Having a wide network is a crucial aspect of grant success. The best option is if you have someone who can give an introduction before you apply. 
		If that’s not possible, do what you can to reach out to the foundation or funder before applying. In the end, however, the most important thing to do is 
		simply apply for every grant that fits your project.</br></br>
		<strong><i>Good luck, and get those grants!</i></strong></br></br>
	</p>
