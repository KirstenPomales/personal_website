  <h2 style="margin-left: 5px; margin-right: 5px; margin-top: 10px;">&bull; Core Values &bull;</h2>
    <p style="text-align: justify; margin-left: 5px; margin-right: 5px; margin-bottom: 10px;">Imperfect is perfect. Build in public. If it doesn’t have traction, kill it. People first. There’s no such thing as a bad idea. No such thing as a stupid question. Check your ego at the door. Yes, and. Ideas are cheap and execution is everything. Check your biases. Frequent feedback. Move fast and break things. Trust, transparency, and open communication. Everything is derivative, nothing is original. Follow your gut.</p>


