      <p style="text-align: justify; margin-left: 5px; margin-right: 5px; margin-bottom: 5px;">Ferris Bueller's Day Off (FBDO) is an internet-native founder collective that exists to ideate, iterate on, and ship MVP tools and products that solve big problems with decentralized infrastructure. We believe that web 3 will eat the world and we’re helping make it happen. 

      <p style="text-align: justify; margin-left: 5px; margin-right: 5px; margin-bottom: 5px;"><strong><i>“The question isn't 'what are we going to do', the question is 'what aren't we going to do?'” - Ferris Bueller</i></strong></p>

      <p style="text-align: justify; margin-left: 5px; margin-right: 5px; margin-bottom: 5px;">FBDO is NOT a venture studio. FBDO is NOT a VC. FBDO is a tight-knit collective of passionate founders validating solutions to big problems and spinning out new ventures when the market is right.</p>

      <p style="text-align: justify; margin-left: 5px; margin-right: 5px; margin-bottom: 5px;"><strong><i>"Life moves pretty fast. If you don't stop and look around once in a while, you could miss it." - Ferris Bueller</i></strong></p>

      <p style="text-align: justify; margin-left: 5px; margin-right: 5px; margin-bottom: 5px;">In web 3, you have to move fast. Our members iterate on ideas together, validating and invalidating hypotheses in a rapid-fire manner through shipping FAST and testing the waters often. Members spin out of the collective to pursue scaling the hypotheses that have traction. Ideas we don’t use are free range for other builders to pick up - FBDO is committed to building in public and open-sourcing all things shipped.</p>

      <p style="text-align: justify; margin-left: 5px; margin-right: 5px; margin-bottom: 5px;"><strong><i>“You’re not dying, you just can’t think of anything good to do.” - Ferris Bueller</i></strong></p>

      <p style="text-align: justify; margin-left: 5px; margin-right: 5px; margin-bottom: 5px;">We think that founders experimenting in silos results often become victims of their own biases. We’re united to hold each other accountable, checking our egos at the door, and add diverse perspectives as we iterate together. We want to be wrong often and right sometimes.</p>

