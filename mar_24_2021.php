		<h4 class="text-left">How Do I Keep People Accountable?</h4>
	</p>
	<p class="text-left">
		Having a system in place to keep everyone on the same page and ensure accountability is a great way to make delegating more efficient and equitable. 
		To avoid getting caught in cycles of unaccountability, find a method to ensure proper communication and expected outcomes. One great tool that can help 
		you achieve this the Accountability Loop — A framework for agreeing on clear deliverables, negotiating details on delivery, clarifying requirements when 
		needed, and receiving delivery of work. This small tool has made a big difference for me; especially when engaging with Virtual Assistants.</br></br>
		Accountability loops clearly convey the responsibilities of all parties and ensure the person delegating has expectations and that the person doing the task commits to their part. 
		Along with expectations and requirements, accountability loops include assessment, and follow-up to determine the outcomes were met. 
		The success in the accountability loop is in its simplicity. The asker and the doer are on the same page and have clearly articulated what they have 
		committed to and when it will be accomplished.</br></br>
		With this process in place blockchain entrepreneurs can delegate anything that is not absolutely necessary for you, the founder, to do; opening up the time and headspace you need to grow your company.
	</p>
