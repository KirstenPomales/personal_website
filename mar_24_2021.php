		<h4 class="text-left">When Should I Start Delegating?</h4>
	</p>
	<p class="text-left">
		The concepts of delegation, scalability, and accountability impact entrepreneurs of all kinds. Lack of delegation hinders scalability and is often so 
		pervasive that it is a bigger barrier for tech founders than other obstacles like software design and building community.</br></br>
		When should you start delegating? I like to use the following question to help me.</br></br>
		What am I doing that…</br></br>
		&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1. Someone else can do well, only cheaper</br></br>
		&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2. I don’t enjoy doing</br></br>
		Criteria 1 Explained: “Well” means: Someone can do the thing as well as you or better than you (not sacrificing quality). 
		Another way to word this is “What tasks are you NOT uniquely needed for”. “Cheaper” means: If you charge your clients $100 per hour and you 
		are doing accounting yourself that could be outsourced for $20 per hour, even if the outsourced accountant takes 3 hours to do the work you could 
		do yourself in 1 hour, hiring the outsourced accountant would still be cheaper.</br></br>
		Criteria 2 Explained: If you really LOVE doing the accounting for your business yourself, hey, I’m not here to stop you. 
		If you don’t love it, it’s a good candidate for outsourcing.</br></br>
		You might be thinking, “This is so broad. There are so many things that fit these criteria, from posting on social media to restructuring the database 
		for the contract I’m working on right now.” — You’d be right!</br></br>
		There are so many opportunities to delegate things big and small. Getting comfortable with delegation early is doing a great favor for your future self: 
		start small, get comfortable with delegating, and “scale up” what you give to others as your company scales.</br></br>
		The first thing I ever delegated (in the context of my businesses, not nonprofit or volunteer activities) was lead generation. 
		I had been putting together lists of companies that were building tools for enterprises; it was taking a ton of time, and I decided to hire a virtual 
		assistant to help do the most labor-intensive and least-technical aspect of the work: researching online and placing the names and websites of each 
		company that met my criteria in the spreadsheet. I still did the analysis of good fit as a prospect myself (as this would have taken time to train the 
		VA on). Nowadays, that same VA I worked with back then has gotten very familiar with my work and can handle analysis tasks for me. 
		This frees up time for me to work on more client projects.</br></br>
		<b><i>With delegation, start small, then scale up.</i></b>
	</p>
