        <br>
        <p class="text-left">
		I can’t shake the feeling that intellectual property NFTs are on the horizon.</br></br>
		NFTs are a type of cryptographic token that represents a unique item (digital or real-life). At this point in time, the NFT digital art and NFT collectibles space have begun to mature. Many new kinds of NFTs outside of the digital art and collectibles space are sprouting up every day.</br></br>
		IP has been increasingly used as collateral over the past few decades; particularly due to the growth in the technology sector and the S&P 500 after the rise of Google, Apple, and Facebook. In 1975, intangible assets accounted for 17% of the market value of S&P 500 companies and grew to 84% by 2015. A signal of the US’ move away from a manufacturing economy to a service and knowledge-based economy.</br></br>
		As a unique and valuable asset, is IP suitable for tokenization? In this article, we’ll discuss how IP-backed finance works today, and explore some of the benefits of and barriers to minting IP NFTs.</br></br>
		<i>None of this is legal, financial advice, friends! Just an exploration of the intersection of NFTs and U.S. IP regulation.</i>
	</p>
