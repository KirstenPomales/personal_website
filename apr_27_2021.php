	</p>
	<p class="text-left">
		<h4 class="text-left">How IP-Backed Financing Works Today</h4>
	</p>
	<p class="text-left">
		IP-backed financing is the use of a company’s intangible assets as collateral when looking to raise debt financing. 
		Similar to how a homeowner assigns their home as collateral to a mortgage, companies will assign the patents they have on their intangible 
		assets as collateral to loans taken out from financing entities. The key difference between the latter two financing options is that 
		the lender of a mortgage will usually hold the borrower’s asset assigned as collateral until the loan is repaid, while the borrower of an 
		IP-backed loan will tend to hold onto their collateral, but give a security interest to the lender to claim it in the event of a default. 
		This method is called securitization and is the most prevalent method used in IP-backed financing.</br></br>
		The latter method relieves the responsibilities of managing the IP, filing infringement lawsuits, defending against infringement lawsuits, 
		and paying patent maintenance fees from the lender back to the borrower, who is the one leveraging the asset and fully understands it. 
		The security interest guarantees ownership of the IP asset to the lender if the borrower defaults; therefore, the lender must then assume 
		all responsibilities of the IP asset.</br></br>
		Startups and small-to-medium-sized enterprises will use their intangible assets as collateral to raise capital, 
		especially if it’s the only valuable asset they have available. Larger corporations will look towards IP-backed financing in times of 
		economic uncertainty. The 2008 financial crisis saw a large boom in IP-backed financing and notably, during this time, General Motors and 
		Chrysler took out IP-backed loans from the US Treasury.</br></br>
	</p>
	<p class="text-left">
		<h4 class="text-left">Methods of IP-backed Financing</h4>
	</p>
	<p class="text-left">
		• <b>Securitization:</b> IP assets are placed into a special purpose vehicle (SPV), a legal subsidiary set up by the parent company. &nbsp;&nbsp;The assets and their associated cash flows are then securitized with the securities given to the lenders. This funding &nbsp;&nbsp;option mitigates risk for both parties because the SPV is a separate entity from the company and lenders don’t need to &nbsp;&nbsp;hold onto the assets themselves. Terms of security repurchasing can be negotiated so the company can reclaim its &nbsp;&nbsp;ownership.</br></br>
		• <b>Direct Collateral:</b> IP assets are assigned directly as collateral to the lender. Cash flows of IP portfolios and licensing &nbsp;&nbsp;agreements are used to secure a loan. The lender has the right to seize the assets in the case of a default.</br></br>
		• <b>Sale-and-Leaseback:</b> A company sells its IP assets to a lender for immediate funding while maintaining a license to use &nbsp;&nbsp;the assets. The lender is entitled to royalty payments through the life of the transaction term. At the end of the term, the &nbsp;&nbsp;company will usually have the option to repurchase their assets at a predetermined price.</br></br>
		• <b>Venture Debt:</b> The most commonly used method. A loan is issued to the borrower, who assigns their IP assets as collateral. &nbsp;&nbsp;The loan must be repaid with interest. Simultaneously, equity warrants are issued to the lender so they don’t take on the &nbsp;&nbsp;risk of owning the assets. A security interest is provided by the borrower to the lender that grants them the right to seize &nbsp;&nbsp;all collateral assets in the event of default.</br></br>
		All this being said, IP as collateral is a growing trend that I don’t see going away soon, as companies keep looking for more ways to boost growth through quick capital influxes.</br></br>
	</p>
	<p class="text-left">
		<h4 class="text-left">Potential Benefits of IP NFTs</h4>
	</p>
	<p class="text-left">
		There are many potential benefits of IP NFTs:</br></br>
		• Opens Up More Capital for the Minter: Rather than taking an IP-backed loan, a minter can raise capital through selling &nbsp;&nbsp;NFTs representing their IP to buyers globally. This has the potential to open up capital access to companies who are less &nbsp;&nbsp;likely to qualify for IP-backed loans traditionally and increase capital access for companies that would have had the option &nbsp;&nbsp;of an IP-backed loan.</br></br>
		• Less Worry of Interest Rates: Interest rates on IP collateralized loans can vary — NFTs could offer a cheaper option for &nbsp;&nbsp;capital raising.</br></br>
		• Collectability for the Buyer: This can open up opportunities for consumers to own a portion of a favorite fast-food &nbsp;&nbsp;company’s trademark, or a piece of a major tech company’s software IP.</br></br>
	</p>
	<p class="text-left">
		<h4 class="text-left">Barriers to Minting and Selling IP NFTs</h4>
	</p>
	<p class="text-left">
		<b>The IP Ownership Regulation Barrier</br></b>
		In most western countries, IP is regulated (ownership is recorded by a government body). Patents, copyrights, trademarks, and other forms of IP fall into the “regulated” category. With ownership being predicated by government records, it would be onerous and logistically difficult to have government records reflect things like fractal ownership and exchanges of IP NFTs. I’m no lawyer, but this seems like the biggest barrier to IP NFTs (then again, the same can be said for real estate NFTs, where ownership of homes must be recorded by a governing body — I haven’t yet figured out how companies are getting by this).</br></br>
		In the US, the main category of IP that is not as regulated is the trade secret. Trade secrets are a secret device, process, or knowledge used by a company in manufacturing its products. Ownership of trade secrets is not recorded in any government registry in the states.</br></br>
		<b>The Conference of Asset Ownership Question</br></b>
		Does owning an NFT representing a company’s patent portfolio grant the holder usage of those patents? If the NFT does not confer ownership of the IP, where does the value of the NFT come from? These are important questions that must be answered before any IP tokenization project gets off the ground.</br></br>
		<b>The Price Discovery Barrier</br></b>
		Even with existing IP-backed loans in traditional finance, valuing IP before determining what loan can be drawn against it is a tricky, imprecise, and sometimes expensive to valuate IP. Traditional market price discovery where a buyer and seller can both transparently see a product, determine their value of it, and negotiate a workable transfer is not as easy with IP, where, for example, the act of the buyer accessing a trade secret could result in the trade secret no longer being a trade secret.</br></br>
	</p>
