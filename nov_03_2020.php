    <div class="container">
      <div class="section-heading text-center">
        <h3 class="text-left" style="font-weight: bold; margin-top: 50px;">Tricks and Traps in Enterprise Blockchain Stakeholder Engagement</h3>
        <h5 class = "text-left">The Top Three Coalition Patterns (And What You Must Know About Them)</h5>
        <h6 class="text-left">Posted on November 3, 2020</h6>
        <br><br>
        <p class="text-left">
          <strong>What’s the best way to unite stakeholders around your enterprise blockchain project?</strong>
          <br><br>
          Partner with other companies to launch a consortium, 
          then jointly fund and develop a distributed ledger technology (DLT)?
          <br><br>
          Is it better to hold off involving other stakeholders until after your blockchain project is underway — so you retain more control?<br>
          <br>
          Or should you sell a blockchain-based solution to clients directly, without a coalition at all?<br>
          <br>
          While there’s no one-size-fits-all way for companies to build their enterprise blockchain projects, 
          most DLTs follow one of three <b>coalition patterns.</b> Each pattern has its advantages and its difficulties 
          depending on the company’s goals for the technology.
          <br><br>
          <b>In this article, we’ll first review these three patterns so you can identify which best applies to your blockchain solution. 
          Then, we’ll discuss common problems that companies who fit each pattern experience. When you’re finished reading, 
          you’ll be able to understand the opportunities that come with each pattern and avoid their common pitfalls.</b>
          <br>
          <br>
          Let's Begin.

          <br><br></p>
          
          <h3 class = "text-left">Coalition Pattern 1: Solo Solution</h3>
          <p class="text-left">
            In this pattern, a single company that sells a blockchain-based solution directly to clients. 
            Whether through a SaaS, a white-label, or another model, the company provides blockchain-based tech 
            in a manner consistent with other traditional “enterprise software”.
          <br>
          <br>
          Many “Solo Solutions” use blockchain-based tech to help one client company streamline inter-business 
          transactions via a ledger. For example, <a href = "https://www.salesforce.com/ap/products/platform/products/blockchain/">Salesforce Blockchain</a> offers clients a custom application builder 
          that extends Salesforce CRM access to their clients’ network of partner organizations. While many “Solo Solutions” 
          are often not true “distributed ledger technologies” due to their control and maintenance lying on the shoulders of 
          the solution provider (Salesforce in this the example above), they still provide clients with some of the benefits of a
          shared ledger; notably, the ability to record multi-party transactions or otherwise share data.
          <br>
          <br>
          Other “Solo Solutions” use blockchain-based technology in a manner similar to a traditional database; 
          allowing a client company to store their own records on a ledger. 
          We’ll discuss in the final section of this article why these solutions often miss the target.
          <br>
          <br>
          In the other two coalition patterns to follow, multiple companies collaborate with transparency, 
          shared responsibility, and shared risk.
          <br><br>
          Before we jump into the other two patterns, let’s review some of the advantages and 
          difficulties that “Solo Solution” projects experience.
          <br><br>
        </p>
        <h4 class = "text-left bold">Pattern 1 Opportunities:</h4>
        <p>
          <ul class="text-left">
            <li>Pattern 1 blockchain solutions are often more palatable to enterprises, 
              who are used to traditional enterprise software models and may be wary of full distributed ledger systems</li>
            <li>Due to a solution provider hosting nodes and maintaining the blockchain-based system, 
              there is a very low barrier to entry for client businesses to integrate with the technology.</li>
            </ul>
            <br>
        </p>
        <h4 class = "text-left bold">Pattern 1 Pitfalls:</h4>
        <p>
          <ul class="text-left">
            <li>Blockchain technology managed by one company is functionally identical to a typical database.</li>
            <li>As multi-stakeholder distributed ledger networks become the norm, 
              Solo Solution blockchain software is likely to become obsolete.</li>
            </ul>
  
            <br><br>
        </p>

        <h3 class="text-left">Coalition Pattern 2: Founder Comes First</h3>
        <br>
        <p class="text-left">
          In “Founder Comes First” projects, the tech often comes before the coalition. 
          A single company builds a blockchain solution, then later founds a consortium or association and works to 
          involve other stakeholders in the solution.
          <br>
          <br>
          The notable difference between “Solo Solution” and “Founder Comes First” projects is that “Founder Comes First” 
          projects want to eventually distribute the solution they’re building to more than one client. This allows member 
          companies to be able to see the full benefits of blockchain technology through the joint maintenance of a shared 
          ledger (or single source of truth).
          <br>
          <br>
          Founding companies often retain greater control, which can lead to problems onboarding coalition 
          partners later in the scaling process, who fear they may not be treated equitably. While a single company 
          is the clear driver of the projects that meet this pattern, stakeholders must be consulted early and often to breed success.
          <br>
          <br>
          <a href="https://www.tradelens.com/about">Maersk Tradelens</a>, is a good example of both what can be done wrong, 
          and what can be done right when designing a “Founder Comes First” blockchain system. 
          In 2017, one of the largest international trade logistics companies, 
          Maersk, built a blockchain-based freight logistics system. They waited until after the technology and governance 
          had been designed to begin engaging stakeholders they hoped to onboard to the platform. Prospective member 
          companies were hesitant to engage because they feared that Maersk may have baked in advantages for itself in 
          the platform; they also believed they didn’t have enough say in the system’s governance. After Maersk analyzed 
          its pitfalls (not involving stakeholders early enough, and giving them the delegated control they needed), 
          Maersk scrapped their initial platform, and re-launched Tradelens — only this time, with heavy amounts of 
          input from as many industry players as possible. Governance was re-designed in a manner that instilled trust 
          and buy-in amongst parties involved. Since the re-launch, Tradelens has integrated hundreds of ports, cargo ships, 
          and shipping partners on one platform over the past few years.
          <br>
          <br>
          Let’s briefly review what works and what doesn’t for companies founding a coalition later in project development.
          <br>
          <br>
        </p>

        <h4 class = "text-left bold">Pattern 2 Opportunities:</h4>
        <p>
          <ul class="text-left">
            <li>Founding companies can initiate the development of a blockchain solution, 
              and reap the rewards of a jointly-verified single source of truth through onboarding additional 
              stakeholders as validators.</li>
            <li>“Founder Comes First” projects can offer an innate “baked in” 
              advantage to the tech’s creator (which can be a good and bad thing!)</li>
            <li>Due to having one clear leader, “Founder Comes First” projects 
              are able to make decisions and launch solutions with more agility (in comparison with pattern 3 projects)</li>
            </ul>
            <br>
        </p>
        <h4 class = "text-left bold">Pattern 2 Pitfalls:</h4>
        <p>
          <ul class="text-left">
            <li>Pattern 2 is a balancing act between pursuing what the founding company believes to be 
              best and involving stakeholders in intimate design and development conversations.</li>
            <li>Without sufficient transparency, 
              trust between the project creator and other stakeholders often 
              breaks down or is never sufficiently established.</li>
            <li>
              Governance design and maturity can become complex as new stakeholders are onboarded to the solution.
            </li>
            <li>
              If other stakeholders are not invited to
              participate in all decision-making for the project’s future, they’re unlikely to buy into the coalition.
            </li>
          </ul>

            <br><br>
        </p>

          <h3 class = "text-left">Coalition Pattern 3: Teamwork Makes the Tech Work</h3>
          <p class="text-left">
            In “Teamwork Makes the Tech Work” projects, multiple companies form a consortium (contractual or entity based) to fund, 
            develop, and jointly maintain a blockchain solution <span style = "font-style: italic;">from the get-go.</span> 
            This often happens organically amongst organizations that are already involved in industry groups together. 
            They’re friendly competitors, each other’s vendors, or partners on some other project. When they hear 
            about the possibilities of blockchain tech for their industry, conversations result in a concept.
          <br>
          <br>
          The first step is usually forming an exploratory group, which evolves over time into a formal association or consortium. 
          Early coalition partners agree on the idea, then pool resources for research and development. 
          A perfect example is <a href="https://www.melloddy.eu/">Melloddy Consortium</a>, co-founded by multiple pharmaceutical companies. 
          Each stakeholder contributed to fund and develop Melloddy’s artificially intelligent, 
          blockchain-based drug discovery system. Once governance was established, 
          Melloddy onboarded new coalition members to scale further.
          <br>
          <br>
          Pattern three shares no pitfalls with the other two coalition strategies. 
          That doesn’t mean these early consortium projects are problem-free — it simply means the 
          issues and advantages are different.
          <br>
          <br>
          </p>

          <h4 class = "text-left bold">Pattern 3 Opportunities:</h4>
        <p>
          <ul class="text-left">
            <li>Interested parties are involved from the outset to further ensure project success.</li>
            <li>Trust and stakeholder buy-in are generally high in founding companies.</li>
            <li>Multiple stakeholders means each must invest less time and fewer resources than pattern one and two projects.</li>
            <li>Member companies are able to see the full benefits of blockchain technology through the joint maintenance of a shared ledger.</li>
            </ul>
            <br>
        </p>
        <h4 class = "text-left bold">Pattern 3 Pitfalls:</h4>
        <p>
          <ul class="text-left">
            <li>When five to ten stakeholders must agree on a decision, the consensus process takes longer.</li>
            <li>Because no one company has a majority interest, these projects can get deprioritized or postponed.</li>
            <li>
              Consortia with no clear leader move slowly, 
              sometimes taking as many as five years to progress from a focus group to a workable blockchain platform.
            </li>
          </ul>

            <br><br>
        </p>
        
        <h3 class = "text-left">Blockchain Tech Is Inherently Multi-Stakeholder (So Yours Should Be, Too)</h3>
        <p class = "text-left">
          2020 marked a transition from a surplus of pattern one projects to a growing number of pattern two and three projects. 
          The value of active, multi-stakeholder collaboration in blockchain systems is impossible to deny. 
          This is because only blockchain platforms that are built and used by multiple companies can realize 
          the full potential of blockchain tech: a jointly verified single source of truth. <b>Without multiple 
          stakeholders to jointly use and maintain an immutable ledger by mutually verifying transactions, a 
          blockchain project offers no more benefit than a traditional database.</b>
          <br>
          <br>
          As you develop the roadmap for your enterprise DLT, be intentional about how you 
          involve stakeholders in your design, scaling, governance, and system maintenance.
