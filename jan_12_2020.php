        <h3 class="text-left" style="font-weight: bold; margin-top: 90px;">Partnerships Matter for Blockchain Startups</h3>
        <h6 class="text-left">Posted on January 12, 2020</h6>
        <br><br>

        <p class="text-left" style="font-weight: bold;">
          TL;DR: Blockchain software startups can learn a lot about growth-hacking by observing startups in other industries. One great growth tactic: build B2B partnerships that help you reach new users. Through observing case studies in consumer electronics and beyond, blockchain startups can learn how to leverage B2B partnerships to increase user growth.
          <br><br>
        </p>
        <h4 style="font-weight: bold;" class="text-left">B2B Partnerships Matter</h4>
        <p class="text-left">
          The B2B partnership is a classic growth strategy that has been successfully leveraged by companies for decades to increase usership and achieve the scalability that many blockchain startups dream about. While partnering with other companies is an extremely powerful tool, many blockchain startups are not currently leveraging it. The other week someone shared with me a powerful example of a B2B partnership that yielded strong returns in the consumer electronics industry. Even though the two industries are very different, this case study can lend some powerful insights for blockchain software founders chasing sustainable user growth.
          <br>
          <br>
        </p>

        <h4 style="font-weight: bold;" class="text-left">The Demo: A Case Study</h4>
        <p class="text-left">
          This case study involves a high-end speaker startup and an audio production company. Both companies were trying to reach distributors that could connect them to consumers. Both companies had similar marketing and sales strategies — they primarily acquired distribution agreements by having a presence at industry trade-shows. Both companies had attended these trade shows in the past, but the real magic happened when the speaker startup approached the audio production company for a partnership. The pitch was simple — work together to host a live demo at the next trade show to allow both companies to display their products in a complementary way. After some discussion, they decided to partner.
          <br><br>
          At the next trade show, the two set up a large demo that drew a lot of attention — by working together, the speaker startup showed off their quality equipment by playing samples from the audio production company’s portfolio. Both companies exited the trade show with many more contracts than they had acquired in prior years. More partnerships like this helped dramatically kick start their growth.
          <br><br>
          But what can blockchain software founders learn from these two audio companies? Let’s take a look at what key attributes made this partnership successful.
          <br>
          <br>
        </p>

        <h4 style="font-weight: bold;" class="text-left">Key Attributes of Successful B2B Partnerships:</h4>
        <ul class="text-left">
          <li>Aligned Target Audience: Both companies were trying to reach the same audience — distributors of high-end audio tools. Partnering for a demo allowed potential clients that would have only approached one company to approach both companies. Because each company was trying to reach the same audience, this resulted in pre-validated leads for both companies.</li>
          <br>
          <li>Complimentary Product: Each company’s product helped highlight the positive aspects of the other company’s product. Through partnering, each company was able to increase their own desirability.</li>
          <br>
          <li>Thoughtful Relationship: Both companies took the time to understand the needs and desires of one another before formally partnering. This allowed them to easily leverage each other’s comparative advantages, avoid conflicts of interest, develop a written partnership agreement, and successfully help each other grow.</li>
        </ul>
        <br>
        <p class="text-left">
          By following a similar formula, blockchain software founders can build partnerships that are just as impactful. Join me in a few hypotheticals...
        </p>
        <br><br>

        <h4 style="font-weight: bold;" class="text-left">Hypothetical A: The Hackathon</h4>
        <p class="text-left">
          Your company has built a game engine that offers out-of-the-box integrations to major decentralized in-game-asset trading platforms.
        </p>
        <br>
        <ul class="text-left">
          <li>Your target audience is developers who are building on other popular game engines and aren’t happy with their low ease-of-interoperability with decentralized trading platforms.</li>
          <br>
          <li>Your product adds explicit value for in-game-asset trading platforms, making them a natural partner for your company.</li>
          <br>
          <li>Your partnership strategy could include approaching one for a jointly sponsored online hackathon that includes a brief demo on how to integrate your two platforms.</li>
        </ul>
        <br>
        <p class="text-left">
          Through partnering, you both are better able to increase awareness of your platforms and quickly onboard new users.
        </p>
        <br><br>

        <h4 style="font-weight: bold;" class="text-left">Hypothetical B: The Audit</h4>
        <p class="text-left">
          Your company has built a tool that uses algorithmic game theory to perform security and sustainability audits on existing blockchains.
        </p>
        <br>
        <ul class="text-left">
          <li>Your target audience is companies that have implemented blockchain solutions but are not sure they are sustainable.</li>
          <br>
          <li>A potential partner with a complimentary offering could be a blockchain governance consultancy.</li>
          <br>
          <li>Your partnership strategy could involve collaborating with a governance consultancy to co-develop a 3-minute online security and governance self-audit that leverages some of your technology.</li>
        </ul>
        <br>
        <p class="text-left">
          Through partnering, you both are better able to catch the attention of new clients by highlighting each party’s expertise and providing the community with value.

          While the examples above are just hypotheticals, founders and marketers in the blockchain space should heavily consider creating a B2B partnerships strategy. Collaborative partnerships are integral to not only the growth of individual companies but the growth of the blockchain industry as a whole. Whether you’ve built a high-end speaker or a decentralized e-commerce solution; mass adoption might depend on the strength of your partnerships.
        </p>
