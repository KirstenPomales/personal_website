        <h3 class="text-left" style="margin-top: 50px;">How to Build Scalable DLTs with Human-Centric Blockchain Design</h3>
        <h6 class="text-left">Posted on Feb 1 2021</h6>
        <br><br>
        <p class="text-left">
        All too often, I see founders in the blockchain space making the same mistake; a lack of human-centric design.</br></br>
		I’m talking about intimately involving who you’re project is serving in the process of designing, launching, and maintaining it.</br></br>
		If you don’t talk to those you intend to serve prior to designing your solution, you set yourself up for failure.</br></br>
		To build solutions that work, you must meet your target market, inspire people to co-create with you, and launch and iterate together.</br></br>
		Every stage of development should involve your target client and getting feedback from the people who are actually going to be using it along the way.</br></br>
		<strong>Blockchain systems cannot scale without stakeholder buy-in.</strong> A lot of blockchain platforms start out with a single founder or group of co-founders trying to do it all; this is a natural start, but it’s necessary to try to get as many stakeholders as you can engaged as soon as possible.</br></br></p>
		<h3 class="text-left">Learning from Maersk</h3></br>
    <p class="text-left">
		Maersk is one of the largest international shipping supply chain logistics companies with tens of thousands of partners all over the world. Up until recently, a lot of their partner’s cargo tracking processes were still manual, making it a really fantastic use case for blockchain technology.</br></br>
		In 2017, Maersk and IBM unveiled that they were creating this cross-border supply chain solution built with blockchain technology. It claimed to have the best tech and was very well-funded, and publicized. They went ahead and built the solution and brought it to a few of Maersk’s key partners that they work with on a day-to-day basis who they wanted to have putting transactions in the blockchain system. Unfortunately, they faced an uphill battle.</br></br>The partner companies who they were trying to pitch to were curious why Maersk didn’t involve them earlier. They were worried that Maersk may be built in some sort of comparative advantage for Maersk and it’s reasonable to believe considering they’re a competitor that never engaged in something like this before. They were a bit leery; they weren’t quite happy with the governance model that Maersk had laid out. They wanted more control over things like node hosting and voting on the future of the system. All of these qualms led Maersk and IBM to decide to go a slightly different route.</br></br>
		They decided to scrap the first project and rebuild - this time, with their stakeholders at the table. Together, they designed new governance, and designed this new system. That was extremely effective for them.</br></br> Today, four out of five of the world’s largest port companies are members of <a href="https://www.tradelens.com/">Maersk Trade Lens</a>. They have over 150 members including more than ten government agencies. The re-launch was successful because the stakehodlers were involved every step of the way.</br></br>
		</p>
    <h3 class="text-left">A More SCRUMmy Go-To-Market Strategy</h3></br>
    <p class="text-left">
		Start your design with your partners on board through each step of the process. Most blockchain systems are designed as follows: design, build, and launch, then onboard your member companies later.
		<img src = "img/howis.png" style="width: 100%; height: auto; padding-bottom: 40px; padding-top: 60px;" alt="Table" class = "img-center">
		This is NOT in line with modern software development methods; most specifically, SCRUM and Agile development which focus on client engagement at all stages of the development lifecycle. <br><br>For example, if we’re looking at a blockchain consortium you might have a specific founding company that decides that they want to initiate the creation of this consortium to maintain a shared blockchain network. They might go about designing this network, launching it and then going to try and onboard as many partners as possible, as many member companies as possible.</br><br>More times than not, this method leads to problems.</br><br>
		I believe that we should be designing more like this.
		<img src = "img/howshould.ong.png" style="width: 100%; height: auto; padding-bottom: 40px; padding-top: 60px;" alt="Table" class = "img-center">
		Right in the beginning, you want to rally the stakeholders by identifying, understanding the needs of, and starting to work together with the stakeholders that you eventually plan on having part of your blockchain network. Once you’ve started to rally the stakeholders, you might only get a few, but even a few is better than none. You want to co-design, build, and launch your blockchain, while integrating these stakeholders in that process.</br></br>
		</p>
    <h3 class="text-left">Activate & Listen To The Voices That Matter</h3></br>
		<p class="text-left">It can be difficult to approach partner companies before you have something built, and it can be difficult to bring together an advisory group of competitors in one room, but these are the things that blockchains are made of. So, when you’re designing blockchain systems, definitely don’t neglect getting the stakeholders involved at every point. Blockchain systems cannot scale without stakeholder buy-in, so you have to understand the stakeholders’ needs and concerns and involve them in early design processes, if possible.</br></br>
    <h4 class="text-left">Getting Started with Stakeholder Analysis</h4></br>
		<p class="text-left">To simplify the process, conduct a stakeholder analysis to ensure your stakeholders are going to feel it’s the right fit. The first step to stakeholder analysis is to identify your stakeholders. That sounds easy but it actually isn’t for a lot of people. There are a lot more stakeholders for most consortium blockchains then they might realize. There will be some entities in your consortium that are going to affect governance decisions and they are also affected by governance decisions.</br></br>
		Regulators can affect a consortium without voting on anything within the consortium. So, if a regulator in the Bahamas decides to write a new piece of regulation, it’s up to the stakeholders in this consortium to become compliant with that regulation. The regulator is in fact affecting the governance and affecting the operations of this consortium. Here you might also have something of an advisory group. An advisory group is a group that includes potentially future consortium members or other investors that are not necessarily voting members of the consortium, but it’s a group that nonetheless has input on the actions of the consortium.</br></br>
		Once you have that line of contact with these prospective member companies and once you start building that relationship, you need to learn more about them. You can simply ask them to give some guidance in exchange for more information on the group and they can decide whether or not it’s of interest. In that process, you can be conducting stakeholder interviews. These are just asking a slew of questions to your stakeholders to really get a feel of how they’re interested in engaging with the consortium, what their fears are what their risk tolerance is etc.</br></br>
     <h4 class="text-left">When Stakeholders Won't Speak...</h4></br>
		<p class="text-left">If you can’t get a hold of any stakeholders, you can conduct market research and have this supplement your understanding of different stakeholders. It can also be good to talk to people who are in the industries that you’re trying to reach to determine what’s at play, but optimally you’ll be actually talking with representatives of these prospective stakeholders. Market research can be useful but don’t rely on it alone.</br></br>
      <h4 class="text-left">Stakeholder Profiles</h4></br>
		<p class="text-left">Lastly, you’re going to want to create profiles on each of your stakeholders so these are similar to customer profiles and can provide you a really easy reference piece for each of your stakeholders once created. The bottom line is knowing your stakeholders, anticipating their needs, and building your system accordingly.</br></br></p>
    <p class="text-left">By inspiring co-creation and understanding your stakeholders, you will best set your project off on the right foot. Good luck!</br></br></p>
